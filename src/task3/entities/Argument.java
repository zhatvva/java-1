package task3.entities;

public class Argument {
    private final double a;
    private final double b;
    private final double h;

    public Argument(double a, double b, double h) {
        this.a = a;
        this.b = b;
        this.h = h;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getH() {
        return h;
    }
}
